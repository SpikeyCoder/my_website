// KA-2026-05-15-01: name of the reporting group used by `report-to` and
// `Reporting-Endpoints`. The Worker handles the corresponding POST path
// (CSP_REPORT_PATH below) and emits the violation report to the Worker
// log (wrangler tail / Cloudflare Logpush), so CSP regressions surface as
// 204s in the access log instead of disappearing silently in the browser.
const CSP_REPORT_GROUP = 'csp-endpoint';
const CSP_REPORT_PATH = '/api/csp-report';

const CONTENT_SECURITY_POLICY = [
  ..._CSP_BASE,
  // No 'unsafe-inline' here. PR #25 will join in the build-time-computed
  // sha256 hash list (one per remaining inline <script> data island) and
  // splice it into this directive.
  // Strict script-src for the report-only (and eventually enforced) policy.
  // Splices in the build-time-computed hash list so legitimate inline
  // <script type="application/ld+json"> / <script type="application/json">
  // data islands and per-blog JSON-LD blobs are permitted by exact-content
  // hash without needing 'unsafe-inline'.
  ["script-src 'self' https://cdn.jsdelivr.net https://gc.zgo.at"]
    .concat(CSP_INLINE_SCRIPT_HASHES)
    .join(" "),
  // KA-2026-05-15-01: telemetry directives. `report-to` is the modern
  // Reporting API directive (named group below); `report-uri` is the
  // legacy directive kept for older browsers (Safari < 16.4 / Firefox
  // < 110). Both point at the same first-party endpoint handled by this
  // Worker so a misbehaving extension or compromised CDN does not also
  // get to drop violation telemetry on the floor.
  `report-uri ${CSP_REPORT_PATH}`,
  `report-to ${CSP_REPORT_GROUP}`,
].join('; ');

// Other security headers — flat object so headers can be merged into the
// upstream response in one pass without overwriting the asset's Content-Type
// or Cache-Control.
const SECURITY_HEADERS = {
  // 2 years, includeSubDomains, preload — closes WA-2026-05-05-04 equivalent
  // for kevinarmstrong.io. Submit to hstspreload.org once this has been live
  // for one full max-age window.
  'Strict-Transport-Security': 'max-age=63072000; includeSubDomains; preload',
  'X-Content-Type-Options': 'nosniff',
  'X-Frame-Options': 'DENY',
  'Referrer-Policy': 'strict-origin-when-cross-origin',
  'Permissions-Policy':
    'camera=(), microphone=(), geolocation=(), interest-cohort=()',
  'Cross-Origin-Opener-Policy': 'same-origin',
  // KA-2026-05-13-02: Cross-Origin-Embedder-Policy: credentialless.
  // Pairs with the existing COOP: same-origin to put HTML documents into a
  // cross-origin isolated context, which (a) hardens against Spectre-style
  // cross-origin side-channel attacks on shared-memory features and (b)
  // surfaces accidental loading of cross-origin subresources without
  // explicit opt-in (CORP / CORS). We pick 'credentialless' rather than
  // 'require-corp' because the only third-party subresource the document
  // currently pulls (gc.zgo.at analytics beacon) does not return a CORP
  // header today; 'credentialless' strips ambient cookies on that hop
  // instead of blocking it outright. If/when every cross-origin
  // dependency advertises a CORP header, this can be tightened to
  // 'require-corp'.
  'Cross-Origin-Embedder-Policy': 'credentialless',
  // KA-2026-05-12-04: add Cross-Origin-Resource-Policy. Same-origin matches
  // the existing COOP posture and prevents cross-origin sites from
  // embedding our subresources cross-credentialed. CORP defaults to
  // 'cross-origin' for static-CDN-served images on Cloudflare Pages, but
  // because the site only serves first-party assets the tighter value is
  // safe today and surfaces accidental cross-origin embedding regressions
  // immediately.
  'Cross-Origin-Resource-Policy': 'same-origin',
  // X-XSS-Protection: explicitly disable legacy browser XSS auditor.
  // Modern guidance (OWASP Secure Headers Project) recommends setting
  // this to 0 so that older Chromium/IE/Safari versions do not attempt
  // their built-in heuristic XSS filter — which has produced more bypass
  // vectors over the years than it has prevented. The strict CSP above
  // is the live XSS defense.
  'X-XSS-Protection': '0',
  // KA-2026-05-15-01: Reporting-Endpoints header is the modern Reporting
  // API counterpart to the CSP `report-to` directive. Named group must match
  // CSP_REPORT_GROUP above. The Worker accepts violation POSTs at
  // CSP_REPORT_PATH and logs them; production reports are visible via
  // `wrangler tail` and Cloudflare Logpush. See compliance/csp-reporting.md.
  'Reporting-Endpoints': `${CSP_REPORT_GROUP}="${CSP_REPORT_PATH}"`,
  // Enforced. The strict policy (no 'unsafe-inline' on script-src)
  // is now the live security boundary; the meta tag has been removed from
  // index.html and 404.html in this PR.
  'Content-Security-Policy': CONTENT_SECURITY_POLICY,
};

// Only attach security headers to HTML / SVG document responses. Static
// assets (CSS, JS, images, fonts) inherit upstream caching headers and don't
// need CSP — and adding HSTS to a 304 / opaque response can confuse some
// clients.
function isHtmlResponse(response) {
  const ct = response.headers.get('content-type') || '';
  return (
    ct.includes('text/html') ||
    ct.includes('application/xhtml+xml') ||
    ct.includes('image/svg+xml') ||
    ct === ''
  );
}

function withSecurityHeaders(response) {
  // Always set HSTS — it's a domain-level signal, not document-specific.
  // CSP and document-only headers gate on isHtmlResponse() to avoid noise.
  const headers = new Headers(response.headers);
  headers.set(
    'Strict-Transport-Security',
    SECURITY_HEADERS['Strict-Transport-Security'],
  );
  headers.set('X-Content-Type-Options', SECURITY_HEADERS['X-Content-Type-Options']);

  if (isHtmlResponse(response)) {
    for (const [name, value] of Object.entries(SECURITY_HEADERS)) {
      headers.set(name, value);
    }
  }

  return new Response(response.body, {
    status: response.status,
    statusText: response.statusText,
    headers,
  });
}

